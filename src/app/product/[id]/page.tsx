"use client";
import axios from "axios";
import { useEffect, useState } from "react";
import Navbar from "../../components/Navbar/page";
import Footer from "../../components/Footer/page";

import { useCart } from "../../contexts/CartContext";

interface Product {
    id: number;
    image: string;
    title: string;
    price: number;
    description: string;
}
interface Params {
    id: string;
}

